def get_items(accumulator, disk_dict):
    for k, v in disk_dict.iteritems():
        accumulator.add_property(k, v)


def create_results():
    site_accumulator = ItemsAccumulator()
    site_accumulator.new_item()
    get_items(site_accumulator, IIs7Site)
    accumulator = ItemsAccumulator()
    for setting in IIsWebVirtualDirSetting:
        accumulator.new_item()
        get_items(accumulator, setting)

    iis_accumulator = ItemsAccumulator()
    for iis_setting in IIsWebServerSetting:
        iis_accumulator.new_item()
        get_items(iis_accumulator, iis_setting)

    results = {
        'IIs7Site': site_accumulator.items,
        'IIsWebVirtualDirSetting': accumulator.items,
        'IIsWebServerSetting': iis_accumulator.items,
        'version': version}

    return results


class TestIIS(BaseTestCase):
    def setUp(self):
        self.plugin = IIS()

    def test_process(self):
        results = Mock(get=lambda *_: [StringAttributeObject()])
        self.plugin.iis_version = u'6'
        data = self.plugin.process(StringAttributeObject(), results, Mock())
        self.assertEquals(len(data), 2)
        self.assertEquals(len(data[1].maps), 1)
        self.assertEquals(data[1].maps[0].iis_version, u'6')
        self.assertEquals(data[1].maps[0].sitename, "ServerComment")
        self.assertEquals(data[1].maps[0].statusname, "Name")
        self.assertEquals(data[1].maps[0].title, "ServerComment")

        # test if no iis sites returned, probably means iis not installed
        # and we need to set is_iis to False so IIS template is not used
        data = self.plugin.process(StringAttributeObject(), {'version': CommandResponse([], [], 0)}, Mock())
        self.assertFalse(data[0].is_iis)

    def test_userdata(self):
        results = create_results()
        self.plugin.iis_version = u'8.5'
        data = self.plugin.process(StringAttributeObject(), results, Mock())
        self.assertEquals(data[1].maps[0].iis_version, u'8.5')
        self.assertNotEqual(data[1].maps[0].statusname, 'W3SVC/1')
        self.assertEqual(data[1].maps[0].statusname, 'Default Web Site')
        self.assertEquals(data[1].maps[0].id, 'W3SVC_1')
        self.assertEquals(data[1].maps[0].apppool, 'DefaultAppPool')  # testing if Application Pool is picked up correctly


